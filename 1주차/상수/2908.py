# 두 개의 세 자리 숫자를 입력받고, 공백을 기준으로 분리하여 정수형으로 변환
A, B = map(int, input().split())

# 숫자를 문자열로 변환한 후, reversed()를 사용하여 뒤집고 다시 문자열로 결합
C = ''.join(reversed(str(A)))  # A를 문자열로 변환 후 거꾸로 뒤집은 값 (ex: "734" → "437")
D = ''.join(reversed(str(B)))  # B를 문자열로 변환 후 거꾸로 뒤집은 값 (ex: "893" → "398")

# 뒤집힌 두 숫자를 리스트에 저장
E = [C, D]

# 리스트에서 가장 큰 값을 찾음 (문자열 비교이므로 정수 변환 필요 없음)
F = max(E)  # 문자열 비교 시 사전순으로 큰 값이 더 큰 정수 값 (ex: "437" vs "398" → "437"이 더 큼)

# 결과 출력 (상수가 읽은 가장 큰 숫자 출력)
print(F)
